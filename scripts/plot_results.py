#!/usr/bin/env python3
"""Plot DBWaller benchmark figures from aggregated CSV output.

Expected input is generated by scripts/analyze_results.py.

Outputs:
1) throughput_vs_threads_*.png
2) latency_p95_vs_threads_*.png (falls back to mean if p95 is unavailable)
3) baseline_vs_sharded_grouped_*.png
"""

from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Plot DBWaller benchmark figures")
    group = parser.add_mutually_exclusive_group(required=True)
    group.add_argument("--run-id", help="Run id under results/processed/<run_id>.csv")
    group.add_argument("--latest", action="store_true", help="Use latest CSV in results/processed")
    group.add_argument("--csv", help="Explicit CSV input path")

    parser.add_argument("--root", default=".", help="Repository root")
    parser.add_argument("--write-percent", type=int, default=5, help="Slice for line charts")
    parser.add_argument("--skew-mode", type=int, default=0, help="Slice for line charts (0 uniform, 1 hotspot)")
    parser.add_argument("--grouped-threads", type=int, default=16, help="Thread count used in grouped comparison chart")
    parser.add_argument("--grouped-shards", type=int, default=64, help="Shards used for sharded grouped bars")
    return parser.parse_args()


def latest_csv(processed_dir: Path) -> Path:
    files = sorted([p for p in processed_dir.glob("*.csv") if p.is_file()], key=lambda p: p.name)
    if not files:
        raise FileNotFoundError(f"No CSV files found in {processed_dir}")
    return files[-1]


def parse_int(value: str) -> Optional[int]:
    if value is None or value == "":
        return None
    try:
        return int(float(value))
    except ValueError:
        return None


def parse_float(value: str) -> Optional[float]:
    if value is None or value == "":
        return None
    try:
        return float(value)
    except ValueError:
        return None


def read_rows(csv_path: Path) -> List[Dict[str, Any]]:
    rows: List[Dict[str, Any]] = []
    with csv_path.open("r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for r in reader:
            row = dict(r)
            row["threads"] = parse_int(r.get("threads", ""))
            row["shards"] = parse_int(r.get("shards", ""))
            row["write_percent"] = parse_int(r.get("write_percent", ""))
            row["skew_mode"] = parse_int(r.get("skew_mode", ""))
            row["real_time"] = parse_float(r.get("real_time", ""))
            row["baseline_ops_per_sec"] = parse_float(r.get("baseline_ops_per_sec", ""))
            row["dbwaller_ops_per_sec"] = parse_float(r.get("dbwaller_ops_per_sec", ""))
            rows.append(row)
    return rows


def ensure_matplotlib() -> Any:
    try:
        import matplotlib.pyplot as plt  # type: ignore
    except Exception as exc:  # pragma: no cover
        raise RuntimeError(
            "matplotlib is required for plotting. Install with: pip install matplotlib"
        ) from exc
    return plt


def time_to_us(value: Optional[float], unit: str) -> Optional[float]:
    if value is None:
        return None
    u = (unit or "").lower()
    if u == "ns":
        return value / 1000.0
    if u == "us":
        return value
    if u == "ms":
        return value * 1000.0
    if u == "s":
        return value * 1_000_000.0
    return value


def ops_value(row: Dict[str, Any]) -> Optional[float]:
    if row.get("impl") == "baseline":
        return row.get("baseline_ops_per_sec")
    return row.get("dbwaller_ops_per_sec")


def line_rows(rows: List[Dict[str, Any]], write_percent: int, skew_mode: int) -> List[Dict[str, Any]]:
    out: List[Dict[str, Any]] = []
    for r in rows:
        if r.get("run_type") != "aggregate":
            continue
        if (r.get("aggregate_name") or "") != "mean":
            continue
        if r.get("write_percent") != write_percent:
            continue
        if r.get("skew_mode") != skew_mode:
            continue
        if r.get("threads") is None:
            continue
        if ops_value(r) is None:
            continue
        out.append(r)
    return out


def p95_available(rows: List[Dict[str, Any]], write_percent: int, skew_mode: int) -> bool:
    for r in rows:
        if (
            r.get("run_type") == "aggregate"
            and (r.get("aggregate_name") or "") == "p95"
            and r.get("write_percent") == write_percent
            and r.get("skew_mode") == skew_mode
        ):
            return True
    return False


def latency_rows(rows: List[Dict[str, Any]], write_percent: int, skew_mode: int, want_p95: bool) -> Tuple[List[Dict[str, Any]], str]:
    target_agg = "p95" if want_p95 else "mean"
    out: List[Dict[str, Any]] = []
    for r in rows:
        if r.get("run_type") != "aggregate":
            continue
        if (r.get("aggregate_name") or "") != target_agg:
            continue
        if r.get("write_percent") != write_percent:
            continue
        if r.get("skew_mode") != skew_mode:
            continue
        if r.get("threads") is None or r.get("real_time") is None:
            continue
        out.append(r)
    label = "p95" if want_p95 else "mean"
    return out, label


def impl_label(row: Dict[str, Any]) -> str:
    impl = row.get("impl")
    if impl == "baseline":
        return "baseline(global_mutex)"
    shards = row.get("shards")
    return f"sharded({shards} shards)"


def write_throughput_plot(plt: Any, rows: List[Dict[str, Any]], out_path: Path, write_percent: int, skew_mode: int) -> None:
    grouped: Dict[str, List[Tuple[int, float]]] = {}
    for r in rows:
        label = impl_label(r)
        grouped.setdefault(label, []).append((int(r["threads"]), float(ops_value(r) or 0.0)))

    if not grouped:
        return

    plt.figure(figsize=(10, 6))
    for label, points in sorted(grouped.items(), key=lambda x: x[0]):
        points.sort(key=lambda p: p[0])
        xs = [p[0] for p in points]
        ys = [p[1] for p in points]
        plt.plot(xs, ys, marker="o", linewidth=2, label=label)

    skew_text = "uniform" if skew_mode == 0 else "hotspot"
    plt.title(f"Throughput vs Threads (write={write_percent}%, skew={skew_text})")
    plt.xlabel("Threads")
    plt.ylabel("Ops/sec")
    plt.grid(True, linestyle="--", alpha=0.35)
    plt.legend()
    plt.tight_layout()
    plt.savefig(out_path, dpi=180)
    plt.close()


def write_latency_plot(plt: Any, rows: List[Dict[str, Any]], out_path: Path, write_percent: int, skew_mode: int, latency_label: str) -> None:
    grouped: Dict[str, List[Tuple[int, float]]] = {}
    for r in rows:
        label = impl_label(r)
        us = time_to_us(r.get("real_time"), r.get("time_unit") or "")
        if us is None:
            continue
        grouped.setdefault(label, []).append((int(r["threads"]), us))

    if not grouped:
        return

    plt.figure(figsize=(10, 6))
    for label, points in sorted(grouped.items(), key=lambda x: x[0]):
        points.sort(key=lambda p: p[0])
        xs = [p[0] for p in points]
        ys = [p[1] for p in points]
        plt.plot(xs, ys, marker="o", linewidth=2, label=label)

    skew_text = "uniform" if skew_mode == 0 else "hotspot"
    plt.title(f"Latency ({latency_label}) vs Threads (write={write_percent}%, skew={skew_text})")
    plt.xlabel("Threads")
    plt.ylabel("Latency (microseconds)")
    plt.grid(True, linestyle="--", alpha=0.35)
    plt.legend()
    plt.tight_layout()
    plt.savefig(out_path, dpi=180)
    plt.close()


def grouped_rows(rows: List[Dict[str, Any]], threads: int, sharded_shards: int) -> List[Dict[str, Any]]:
    out: List[Dict[str, Any]] = []
    for r in rows:
        if r.get("run_type") != "aggregate":
            continue
        if (r.get("aggregate_name") or "") != "mean":
            continue
        if r.get("threads") != threads:
            continue
        impl = r.get("impl")
        shards = r.get("shards")
        if impl == "sharded" and shards != sharded_shards:
            continue
        out.append(r)
    return out


def write_grouped_plot(plt: Any, rows: List[Dict[str, Any]], out_path: Path, threads: int, sharded_shards: int) -> None:
    # Build categories by (write_percent, skew_mode)
    categories = sorted({(r.get("write_percent"), r.get("skew_mode")) for r in rows})
    if not categories:
        return

    baseline_vals: List[float] = []
    sharded_vals: List[float] = []
    labels: List[str] = []

    for w, s in categories:
        labels.append(f"w{w}-{'uni' if s == 0 else 'hot'}")

        base = next((r for r in rows if r.get("impl") == "baseline" and r.get("write_percent") == w and r.get("skew_mode") == s), None)
        shard = next((r for r in rows if r.get("impl") == "sharded" and r.get("write_percent") == w and r.get("skew_mode") == s), None)

        baseline_vals.append(float(ops_value(base) or 0.0) if base else 0.0)
        sharded_vals.append(float(ops_value(shard) or 0.0) if shard else 0.0)

    x = list(range(len(labels)))
    width = 0.38

    plt.figure(figsize=(12, 6))
    plt.bar([i - width / 2 for i in x], baseline_vals, width=width, label="baseline(global_mutex)")
    plt.bar([i + width / 2 for i in x], sharded_vals, width=width, label=f"sharded({sharded_shards} shards)")

    plt.title(f"Baseline vs Sharded Ops/sec by Mix and Skew (threads={threads})")
    plt.xlabel("Workload slice")
    plt.ylabel("Ops/sec")
    plt.xticks(x, labels, rotation=0)
    plt.grid(True, axis="y", linestyle="--", alpha=0.35)
    plt.legend()
    plt.tight_layout()
    plt.savefig(out_path, dpi=180)
    plt.close()


def main() -> int:
    args = parse_args()
    root = Path(args.root).resolve()

    if args.csv:
        csv_path = Path(args.csv).resolve()
        run_id = csv_path.stem
    else:
        processed = root / "results" / "processed"
        if args.latest:
            csv_path = latest_csv(processed)
            run_id = csv_path.stem
        else:
            run_id = str(args.run_id)
            csv_path = processed / f"{run_id}.csv"

    if not csv_path.exists():
        raise FileNotFoundError(f"CSV not found: {csv_path}")

    rows = read_rows(csv_path)
    plt = ensure_matplotlib()

    out_dir = root / "results" / "figures" / run_id
    out_dir.mkdir(parents=True, exist_ok=True)

    # 1) Throughput vs thread count
    tr_rows = line_rows(rows, args.write_percent, args.skew_mode)
    throughput_file = out_dir / f"throughput_vs_threads_w{args.write_percent}_s{args.skew_mode}.png"
    write_throughput_plot(plt, tr_rows, throughput_file, args.write_percent, args.skew_mode)

    # 2) p95 latency vs thread count (fallback: mean)
    has_p95 = p95_available(rows, args.write_percent, args.skew_mode)
    lat_rows, lat_label = latency_rows(rows, args.write_percent, args.skew_mode, want_p95=has_p95)
    latency_file = out_dir / f"latency_{lat_label}_vs_threads_w{args.write_percent}_s{args.skew_mode}.png"
    write_latency_plot(plt, lat_rows, latency_file, args.write_percent, args.skew_mode, lat_label)

    # 3) Grouped baseline vs sharded comparison by mix+skew
    grp_rows = grouped_rows(rows, args.grouped_threads, args.grouped_shards)
    grouped_file = out_dir / f"baseline_vs_sharded_grouped_t{args.grouped_threads}_s{args.grouped_shards}.png"
    write_grouped_plot(plt, grp_rows, grouped_file, args.grouped_threads, args.grouped_shards)

    summary = {
        "run_id": run_id,
        "input_csv": str(csv_path),
        "output_dir": str(out_dir),
        "throughput_plot": str(throughput_file),
        "latency_plot": str(latency_file),
        "grouped_plot": str(grouped_file),
        "latency_aggregate": lat_label,
    }
    print(json.dumps(summary, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
